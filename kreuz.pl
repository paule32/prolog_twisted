    eigenschaft(B,E) :- unterbegriff(B,B1), !, eigenschaft(B1,E), not(konflikt(B,E)).

    unterbegriff(B,B1) :- ist_ein(B,B1).
    unterbegriff(B,B1) :- ist_ein(B,B2),  unterbegriff(B2,B1).

eigenschaft(B):-
    findall(E,eigenschaft(B,E),L),
    write('Dieser Begriff hat die folenden Eigenschaften'), nl,
